package geekbrains.androidlevel1;

public class Soc {

    private String description;
    private int picture;

    public Soc(String description, int picture) {
        this.description = description;
        this.picture = picture;
    }

    public String getDescription() {
        return description;
    }

    public int getPicture() {
        return picture;
    }

}
